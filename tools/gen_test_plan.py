def load_achievements(root):
    """Парсит (id, title) всех достижений из definitions.dart."""
    path = os.path.join(root, "flutter", "lib", "achievements", "definitions.dart")
    text = open(path, encoding="utf-8").read()
    ids = re.findall(r"id:\s*'([^']+)'", text)
    titles = re.findall(r"title:\s*'([^']+)'", text)
    pairs = list(zip(ids, titles))
    if len(pairs) != 120:
        print(f"  ! предупреждение: найдено {len(pairs)} достижений (ожидалось 120)")
    return pairs


def crit(items, every=7):
    """Помечает каждый every-й пункт как критический [C] для smoke-среза."""
    return [("[C] " + s) if (i % every == 0) else s for i, s in enumerate(items)]


