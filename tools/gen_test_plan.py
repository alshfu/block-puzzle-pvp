def build_platform(plat, achievements):
    lines = [f"# QA-чеклист BlockDuel 9×9 — {plat['title']}", ""]
    lines.append(textwrap.fill(
        "Полномасштабная матрица ручных проверок. Пункты с [C] — критический "
        "smoke-срез (каждую сборку); остальное — полный регресс. Сгенерировано "
        "tools/gen_test_plan.py — не редактировать вручную, менять генератор.", 78))
    lines.append("")

    sections = shared_sections(plat, achievements) + plat["specific"]
    n = 0
    toc, body = [], []
    for title, items in sections:
        items = crit(items)
        toc.append((title, len(items)))
        body.append(f"## {title}  ({len(items)})")
        body.append("")
        for it in items:
            n += 1
            body.append(f"- [ ] {n}. {it}")
        body.append("")

    lines.append(f"**Всего пунктов: {n}**")
    lines.append("")
    lines.append("## Оглавление")
    lines += [f"- {title} — {cnt}" for title, cnt in toc]
    lines.append("")
    lines += body
    return "\n".join(lines), n


