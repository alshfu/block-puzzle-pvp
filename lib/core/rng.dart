/// rng.dart — детерминированный PRNG `mulberry32` (порт `makeRng` из TS-ядра).
///
/// За что отвечает файл:
///   Единственный источник псевдослучайности в ядре. Воспроизводит алгоритм
///   `makeRng` из `src/core/index.ts` БИТ-В-БИТ, чтобы Dart-порт давал ту же
///   последовательность чисел, что и TS-версия, на обоих таргетах (Dart VM и
///   Web/JS). Это фундамент детерминизма всего ядра: тот же seed → тот же
///   7-bag → та же партия (нужно для golden-тестов, server-authoritative
///   онлайна, реплеев, анти-чита).
///
/// Почему так аккуратно с битами:
///   JS использует 32-битную арифметику (`| 0`, `>>> 0`, `Math.imul`). В Dart
///   на VM `int` — 64-битный, на Web — это JS number. Чтобы результат совпал
///   на обеих платформах, все промежуточные значения держим как беззнаковые
///   32-битные (`& 0xFFFFFFFF`), а 32-битное умножение реализуем вручную
///   через [_imul] (аналог `Math.imul`), избегая переполнения 64-бит.
library;

/// Маска младших 32 бит — приводит значение к беззнаковому 32-битному.
const int _mask32 = 0xFFFFFFFF;

/// Константа шага состояния mulberry32 (та же, что в TS: `0x6d2b79f5`).
const int _increment = 0x6d2b79f5;

/// Делитель для перевода 32-битного целого в double ∈ [0, 1): 2^32.
const double _toUnitDivisor = 4294967296.0;

/// 32-битное умножение «как `Math.imul`»: возвращает младшие 32 бита `a * b`
/// как беззнаковое 32-битное число.
///
/// Произведение раскладывается на половины (hi/lo по 16 бит), чтобы ни одно
/// промежуточное значение не превысило 2^33 — это безопасно и на 64-битном VM,
/// и на Web (где целые надёжны до 2^53).
int _imul(int a, int b) {
  a &= _mask32;
  b &= _mask32;
  final int aLo = a & 0xFFFF;
  final int aHi = (a >>> 16) & 0xFFFF;
  final int bLo = b & 0xFFFF;
  final int bHi = (b >>> 16) & 0xFFFF;
  final int low = aLo * bLo;
  final int mid = (aLo * bHi + aHi * bLo) & 0xFFFF;
  return (low + (mid << 16)) & _mask32;
}

/// Детерминированный генератор псевдослучайных чисел `mulberry32`.
///
/// Хранит 32-битное [_state], каждый вызов [next] продвигает состояние и
/// возвращает следующее число `double` ∈ [0, 1). Полностью эквивалентен
/// замыканию, которое возвращает `makeRng(seed)` в TS-ядре.
class Mulberry32 {
  /// Текущее 32-битное состояние генератора (беззнаковое).
  int _state;

  /// Создаёт генератор от целочисленного [seed] (приводится к 32 битам).
  Mulberry32(int seed) : _state = seed & _mask32;

  /// Восстанавливает генератор из ранее снятого внутреннего состояния [state]
  /// (для детерминистичного resume сохранёнок).
  Mulberry32.fromState(int state) : _state = state & _mask32;

  /// Текущее внутреннее 32-битное состояние (для сериализации).
  int get state => _state;

  /// Возвращает следующее псевдослучайное число `double` ∈ [0, 1).
  ///
  /// Порядок операций повторяет TS построчно; промежуточные результаты
  /// маскируются до 32 бит, чтобы совпасть с JS-семантикой `| 0` / `>>> 0`.
  double next() {
    _state = (_state + _increment) & _mask32;
    int t = _state;
    t = _imul(t ^ (t >>> 15), 1 | t);
    t = ((t + _imul(t ^ (t >>> 7), 61 | t)) ^ t) & _mask32;
    return ((t ^ (t >>> 14)) & _mask32) / _toUnitDivisor;
  }
}

/// Тип «функция-источник случайности», совместимый по смыслу с TS
/// `rng: () => number`. Позволяет ядру принимать любой источник `[0, 1)`.
typedef RandomSource = double Function();

/// Удобная фабрика: создаёт [Mulberry32] и возвращает его метод [Mulberry32.next]
/// как [RandomSource] — прямой аналог TS `makeRng(seed)`.
RandomSource makeRng(int seed) => Mulberry32(seed).next;
